package com.wl.forms;

public class PoPayStatistics {
	private double payMent;
	private double hasPaid;
	private double noPaid;
	private String month;
	public double getPayMent() {
		return payMent;
	}
	public void setPayMent(double payMent) {
		this.payMent = payMent;
	}
	public double getHasPaid() {
		return hasPaid;
	}
	public void setHasPaid(double hasPaid) {
		this.hasPaid = hasPaid;
	}
	public double getNoPaid() {
		return noPaid;
	}
	public void setNoPaid(double noPaid) {
		this.noPaid = noPaid;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	
}
