package cfmes.bom.entity;

public class PartStatus {
     private String status_id;
     private String status_result;
     private String status_do;
	public String getStatus_id() {
		return status_id;
	}
	public void setStatus_id(String statusId) {
		status_id = statusId;
	}
	public String getStatus_result() {
		return status_result;
	}
	public void setStatus_result(String statusResult) {
		status_result = statusResult;
	}
	public String getStatus_do() {
		return status_do;
	}
	public void setStatus_do(String statusDo) {
		status_do = statusDo;
	}
     
}
